const COUNTRY_BY_ISO2 = new Map(
  COUNTRY_OPTIONS.map((country) => [country.iso2.toUpperCase(), country])
);
const COUNTRY_BY_ISO3 = new Map(
  COUNTRY_OPTIONS.map((country) => [country.iso3.toUpperCase(), country])
);

