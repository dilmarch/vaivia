function normalizeName(value: string) {
  return value
    .trim()
    .normalize("NFKD")
    .replace(/[\u0300-\u036f]/g, "")
    .toLowerCase()
    .replace(/&/g, " and ")
    .replace(/[^a-z0-9]+/g, " " )
    .replace(/\s+/g, " " )
    .trim();
}

const COUNTRY_BY_NAME = new Map<string, CountryOption>();
for (const country of COUNTRY_OPTIONS) {
  COUNTRY_BY_NAME.set(normalizeName(country.name), country);
}

export function getCountryOptionByIso3(value: string) {
  return COUNTRY_BY_ISO3.get(value.trim().toUpperCase()) || null;
}

export function getCountryOptionByIso2(value: string) {
  return COUNTRY_BY_ISO2.get(value.trim().toUpperCase()) || null;
}

export function normalizeCountryCode(value: string): string | null {
  const trimmed = value.trim();
  if (!trimmed) return null;

  const upper = trimmed.toUpperCase();
  if (COUNTRY_BY_ISO3.has(upper)) return upper;

  const iso2Match = COUNTRY_BY_ISO2.get(upper);
  if (iso2Match) return iso2Match.iso3;

  const alias = COUNTRY_NAME_ALIASES[normalizeName(trimmed)];
  if (alias && COUNTRY_BY_ISO3.has(alias)) return alias;

  return COUNTRY_BY_NAME.get(normalizeName(trimmed))?.iso3 || null;
}
